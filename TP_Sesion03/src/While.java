
public class While {
    public static void main(String[] args) {
        int num = 0;
        int numero = 0;
        for (int i = 0; i<=20;  i++){
            System.out.println(num);
            num = num +1;
        }
        while(numero <= 20){
            System.out.println(numero);
            numero = numero + 1;
        }
    }
}
