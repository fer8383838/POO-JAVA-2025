public class ClaseMath {
    public static void main(String[] args) {
        System.out.println("Clase Math");
        System.out.println("Raiz cuadrada: " +  Math.sqrt(144) );
        System.out.println("Valor absoluto: " + Math.abs(-7));
        System.out.println("Potencia: " + Math.pow(2,16) );
        System.out.println("Redondeo: " + Math.round(14.97432) );
        System.out.println("Minimo de 2 numeros: "+ Math.min(4,9) );
        System.out.println("Maximo de 2 numeros: "+ Math.max(4,9) );
        System.out.println("Numero Aleatorio: " + Math.round( Math.random()*200) );
    }
}
